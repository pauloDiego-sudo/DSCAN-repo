  // Define helper functions
  const distance = ([x1, y1], [x2, y2]) => Math.sqrt((x1 - x2) ** 2 + (y1 - y2) ** 2);
  const regionQuery = (data, point, epsilon) =>
    data.filter(otherPoint => distance(point, otherPoint) <= epsilon);
  
  const isInCluster = (clusters, point) =>
    clusters.some(cluster => cluster.some(p => p[0] === point[0] && p[1] === point[1]));
  
  // DBSCAN algorithm
  function dbscan(data, epsilon, minPoints) {
    const clusters = [];
    const visited = new Set();
    const noise = [];
  
    const visitKey = ([x, y]) => `${x},${y}`;
  
    const expandCluster = (point, neighbors, cluster) => {
      cluster.push(point);
  
      scatterTracer.set([data, ...clusters, noise]);
      Tracer.delay();
  
      while (neighbors.length > 0) {
        const neighbor = neighbors.pop();
        if (!visited.has(visitKey(neighbor))) {
          visited.add(visitKey(neighbor));
          const neighborNeighbors = regionQuery(data, neighbor, epsilon);
  
          if (neighborNeighbors.length >= minPoints) {
            neighbors.push(...neighborNeighbors);
          }
        }
  
        if (!isInCluster(clusters, neighbor)) {
          cluster.push(neighbor);
          scatterTracer.set([data, ...clusters, noise]);
          Tracer.delay();
        }
      }
    };
  
    for (const point of data) {
      if (visited.has(visitKey(point))) continue;
  
      visited.add(visitKey(point));
      const neighbors = regionQuery(data, point, epsilon);
  
      if (neighbors.length < minPoints) {
        noise.push(point);
        logTracer.println(`Point ${JSON.stringify(point)} classified as noise.`);
      } else {
        const cluster = [];
        clusters.push(cluster);
        logTracer.println(`Starting new cluster with point ${JSON.stringify(point)}.`);
        logTracer.println(`Neighbors found: ${neighbors.map(p => JSON.stringify(p)).join(', ')}.`);
        expandCluster(point, neighbors, cluster);
        logTracer.println(`Finished cluster with ${cluster.length} points: ${cluster.map(p => JSON.stringify(p)).join(', ')}.`);
      }
  
      scatterTracer.set([data, ...clusters, noise]);
      Tracer.delay();
    }
  
    return { clusters, noise };
  }
  
